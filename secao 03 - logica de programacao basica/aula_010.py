# Concatenacao (+) e repeticao (*) com operadores aritmeticos

concatenacao = 'A' + 'B' + 'C'
print(concatenacao)
nome_completo = 'Luiz' + ' ' + 'Otavio' + ' - ' + str(25) + ' ' + 'anos'
print(nome_completo)

# * -> repeticao de caracteres de string
a_dez_vezes = 'A' * 10
tres_vezes_luiz = 3 * 'Luiz'
print(a_dez_vezes)
print(tres_vezes_luiz)
